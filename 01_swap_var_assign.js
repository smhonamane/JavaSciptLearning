var sweety="Sweety"; //initialization
var cutie="Cutie";
var swap; //declaration
console.log("------Before Swapping------\n Sweety\n Cutie\n");
swap=sweety;
sweety=cutie;
cutie=swap;
console.log("------After Swapping------");
console.log(sweety);
console.log(cutie,"\n");


